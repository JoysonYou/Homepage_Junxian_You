const root = document.documentElement;
const button = document.querySelector(".lang-toggle");
const current = document.querySelector(".lang-current");

function getValue(source, path) {
  return path.split(".").reduce((value, key) => value?.[key], source);
}

function setText(lang) {
  const content = profile[lang];
  root.lang = lang === "zh" ? "zh-CN" : "en";
  root.dataset.lang = lang;
  document.title = lang === "zh" ? "尤俊贤 | 博士研究生" : "Junxian You | PhD Student";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValue(content, element.dataset.i18n);
    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  renderList(".about-list", content.about.items);
  renderList(".news-list", content.news.items);
  renderList(".focus-list", content.research.items);
  renderList(".activities-list", content.activities.items);
  renderList(".awards-list", content.awards.items);
  renderPublications(content.publications.items);
  renderTimeline(content.education.items);
  renderProjects(content.projects.items);

  current.textContent = lang === "zh" ? "EN" : "中文";
  localStorage.setItem("junxian-homepage-lang", lang);
}

function renderList(selector, items) {
  document.querySelector(selector).innerHTML = items
    .map((item) => {
      const text = typeof item === "string" ? item : item.text;
      const links = typeof item === "string" ? [] : item.links;
      return `<li>${renderTextWithLinks(text, links)}</li>`;
    })
    .join("");
}

function renderTextWithLinks(text, links = []) {
  const matches = links
    .map((link) => ({ ...link, index: text.indexOf(link.label) }))
    .filter((link) => link.index >= 0)
    .sort((a, b) => a.index - b.index);

  let html = "";
  let cursor = 0;

  matches.forEach((link) => {
    if (link.index < cursor) return;
    html += escapeHtml(text.slice(cursor, link.index));
    html += `<a href="${escapeAttribute(link.url)}" target="_blank" rel="noreferrer">${escapeHtml(link.label)}</a>`;
    cursor = link.index + link.label.length;
  });

  return html + escapeHtml(text.slice(cursor));
}

function renderPublications(items) {
  document.querySelector(".publication-list").innerHTML = items
    .map(
      (item) => {
        const authorsHtml = escapeHtml(item.authors)
          .replaceAll("Junxian You", "<strong>Junxian You</strong>");
        return `
          <article class="publication">
            <span class="pub-badge">${escapeHtml(item.badge)}</span>
            <h3 class="pub-title"><a href="${escapeAttribute(item.url)}" target="_blank" rel="noreferrer">${escapeHtml(item.title)}</a></h3>
            <p class="pub-authors">${authorsHtml}</p>
            <p class="pub-venue">${escapeHtml(item.venue)}</p>
          </article>
        `;
      },
    )
    .join("");
}

function renderTimeline(items) {
  document.querySelector(".timeline").innerHTML = items
    .map(
      (item) => `
        <article class="timeline-item">
          <span class="timeline-date">${escapeHtml(item.date)}</span>
          <h3 class="timeline-title">${escapeHtml(item.title)}</h3>
          <p class="timeline-meta">${escapeHtml(item.meta)}</p>
        </article>
      `,
    )
    .join("");
}

function renderProjects(items) {
  document.querySelector(".project-list").innerHTML = items
    .map(
      (item) => `
        <article class="project">
          <h3 class="project-title">${escapeHtml(item.title)}</h3>
          <p class="project-body">${escapeHtml(item.body)}</p>
        </article>
      `,
    )
    .join("");
}

function escapeHtml(value) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function escapeAttribute(value) {
  return escapeHtml(value || "#");
}

